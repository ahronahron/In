// Minimal password-reset page that accepts a token link (?token=...) and lets the user set a new password.
// Adjust DB credentials if needed.

error_reporting(E_ALL);
ini_set('display_errors', '0');

function h(string $s): string { return htmlspecialchars($s, ENT_QUOTES | ENT_SUBSTITUTE, 'UTF-8'); }

$DB_HOST = '127.0.0.1';
$DB_USER = 'root';
$DB_PASS = '';
$DB_NAME = 'Inventory_system_db';

$mysqli = @new mysqli($DB_HOST, $DB_USER, $DB_PASS, $DB_NAME);
if ($mysqli->connect_errno) {
    http_response_code(500);
    echo 'Database connection error.';
    exit;
}
$mysqli->set_charset('utf8mb4');

$method = $_SERVER['REQUEST_METHOD'] ?? 'GET';
$tokenRaw = trim((string)($_GET['token'] ?? $_POST['token'] ?? ''));

// Basic token sanity check (64 hex chars as generated by send_reset_email)
$validTokenFormat = preg_match('/^[0-9a-f]{64}$/i', $tokenRaw);

$errors = [];
$success = '';

if ($method === 'POST') {
    $pw = (string)($_POST['password'] ?? '');
    $pw2 = (string)($_POST['password_confirm'] ?? '');

    if (!$validTokenFormat) {
        $errors[] = 'Invalid or missing token.';
    }
    if (strlen($pw) < 8) {
        $errors[] = 'Password must be at least 8 characters.';
    }
    if ($pw !== $pw2) {
        $errors[] = 'Passwords do not match.';
    }

    if (empty($errors)) {
        $tokenHash = hash('sha256', $tokenRaw);

        // Find matching user with non-expired token
        $stmt = $mysqli->prepare('SELECT user_id FROM users WHERE password_reset_token = ? AND password_reset_expires >= NOW() AND status = "active" LIMIT 1');
        if ($stmt) {
            $stmt->bind_param('s', $tokenHash);
            $stmt->execute();
            $res = $stmt->get_result();
            $user = $res->fetch_assoc();
            $stmt->close();

            if ($user && isset($user['user_id'])) {
                $newHash = password_hash($pw, PASSWORD_DEFAULT);
                $upd = $mysqli->prepare('UPDATE users SET password = ?, password_reset_token = NULL, password_reset_expires = NULL, updated_at = CURRENT_TIMESTAMP WHERE user_id = ?');
                if ($upd) {
                    $upd->bind_param('si', $newHash, $user['user_id']);
                    if ($upd->execute()) {
                        $success = 'Your password has been updated. You may now <a href="login.php">log in</a>.';
                    } else {
                        $errors[] = 'Unable to update password. Try again later.';
                    }
                    $upd->close();
                } else {
                    $errors[] = 'Server error.';
                }
            } else {
                $errors[] = 'Invalid or expired token.';
            }
        } else {
            $errors[] = 'Server error.';
        }
    }
}

